<template>
  <div class="py-4">
    <Switch v-model="enabled" @click="changeTheme"
      :class="enabled ? 'bg-white hover:border-gray-500 border-[1px]' : 'bg-[#f1f1f1] hover:border-slate-200 border'"
      class="relative inline-flex h-[22px] w-[41px]  shrink-0 cursor-pointer rounded-full border-2  transition-colors duration-200 ease-in-out focus:outline-none focus-visible:ring-2 focus-visible:ring-white/75">
      <span aria-hidden="true" :class="enabled ? 'translate-x-5' : 'translate-x-0'"
        class="pointer-events-none inline-block h-[18px] w-[18px] transform rounded-full bg-white shadow-lg ring-0 transition duration-200 ease-in-out">
        <svg v-if="!enabled" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5"
          stroke="currentColor" class="size-4 text-gray-400	">
          <path stroke-linecap="round" stroke-linejoin="round"
            d="M12 3v2.25m6.364.386-1.591 1.591M21 12h-2.25m-.386 6.364-1.591-1.591M12 18.75V21m-4.773-4.227-1.591 1.591M5.25 12H3m4.227-4.773L5.636 5.636M15.75 12a3.75 3.75 0 1 1-7.5 0 3.75 3.75 0 0 1 7.5 0Z" />
        </svg>
        <svg v-else xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5"
          stroke="currentColor" class="size-4 text-[black/1]">
          <path stroke-linecap="round" stroke-linejoin="round"
            d="M21.752 15.002A9.72 9.72 0 0 1 18 15.75c-5.385 0-9.75-4.365-9.75-9.75 0-1.33.266-2.597.748-3.752A9.753 9.753 0 0 0 3 11.25C3 16.635 7.365 21 12.75 21a9.753 9.753 0 0 0 9.002-5.998Z" />
        </svg>

      </span>
    </Switch>
  </div>
</template>

<script setup>
import { ref } from 'vue'
import { Switch } from '@headlessui/vue'

const enabled = ref(false);

const changeTheme = () => {
  const element = document.querySelector('html');
  if (enabled.value) {
    element.classList.remove('dark');
    element.style.color = 'black';
  } else {
    element.classList.add('dark');
    element.style.color = 'white';

  }
}

</script>
